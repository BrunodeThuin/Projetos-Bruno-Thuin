#include <stdio.h>

int main(){
    float x = 4;
    float y = 3;
    int m = 4;
    int n = 3;

    /* Operadores matemáticos:
       + : adição
       - : subtração
       * : multiplicação
       / : divisão
       % : resto da divisão
    */
    printf("x = %f\ny = %f\n",x,y);

    printf("x + y = %f\n", x + y);
    printf("x - y = %f\n", x - y);
    printf("x * y = %f\n", x * y);
    printf("x / y = %f\n", x / y);
    printf("x %% y = %d\n", m % n);

    return 0;
}
